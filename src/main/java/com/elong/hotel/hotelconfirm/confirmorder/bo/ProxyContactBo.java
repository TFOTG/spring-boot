package com.elong.hotel.hotelconfirm.confirmorder.bo;

public class ProxyContactBo {

	private String proxyId;
	private String mobile;
	private String faxCountry;
	private String faxRegion;
	private String faxNum;
	private String email;

	public String getProxyId() {
		return proxyId;
	}

	public void setProxyId(String proxyId) {
		this.proxyId = proxyId;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getFaxCountry() {
		return faxCountry;
	}

	public void setFaxCountry(String faxCountry) {
		this.faxCountry = faxCountry;
	}

	public String getFaxRegion() {
		return faxRegion;
	}

	public void setFaxRegion(String faxRegion) {
		this.faxRegion = faxRegion;
	}

	public String getFaxNum() {
		return faxNum;
	}

	public void setFaxNum(String faxNum) {
		this.faxNum = faxNum;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
}
