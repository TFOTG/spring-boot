package com.elong.hotel.hotelconfirm.confirmorder.enums;

/**
 * 反馈动作名称枚举
 */
public enum ServerRunningDomainEnum {

	/**
	 * 消息监听
	 */
	MESSAGE_LISTENING("消息监听", 1),

	/**
	 * 时间戳变化
	 */
	TIMESTAMP_CHANGE("时间戳变化", 2);

	ServerRunningDomainEnum(String name, int actionIndex) {
		this.name = name;
		this.actionIndex = actionIndex;
	}

	// 行为名称
	private String name;
	// action执行优先级
	private int actionIndex;

	public int getActionIndex() {
		return actionIndex;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
